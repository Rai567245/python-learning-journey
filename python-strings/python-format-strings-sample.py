# This program demonstrates the use of f-strings for formatting strings in Python.

#F-String Example
first_name = "John"
last_name = "Doe"
full_name = f"{first_name} + {last_name}"
print(full_name)

# Plaveholder Variables
name = "Alice"
age = 30
nameAgeInfo = f"{name} is {age} years old."
print(nameAgeInfo)

