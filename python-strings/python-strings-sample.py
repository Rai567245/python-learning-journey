# This program demonstrates the uses of python strings, this includes the following:

# Strings
print("Hello, World!")
print('Hello, World!')
      
# Quote Inside Qoutes
print("He said, 'Hello!'")
print('She said, "Hi!"')

# Assigning a String to a Variable
message = "Hello, Python!"
print(message)

# Multiline Strings
multiline_message = """This is a
multiline string."""
print(multiline_message)

# String Concatenation
greeting = "Hello, "
name = "Alice"
full_greeting = greeting + name
print(full_greeting)