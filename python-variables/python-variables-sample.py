# Creating variables in python
message = "Hello, Python!"
number = 19

print(message)
print(number)

# Changing the value of a variable
message = "Hello, World!"
print(message)

# If you want to specify the type of variable, you can use type casting
age = int(19)
height = float(5.7)
weight = float(55.25)

print("Age:", age)
print("Height:", height)
print("Weight:", weight)