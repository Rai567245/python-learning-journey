# Output Variables Sample

inGameNameAndLevel = "Rai011906 (Level 19)"

print("IGN & Level:", inGameNameAndLevel)

# You can also separate multiple variables and its values in a single print statement by using commas.
inGameName = "Rai011906"
inGameLevel = 19

print("IGN:", inGameName, "| Level:", inGameLevel)

# You can also separate multiple variables and its values in a single print statement by using plus sign operator.
inGameName = "Rai011906 "
inGameLevel = "(Level 19)"

print("IGN & Level: " + inGameName + inGameLevel)
