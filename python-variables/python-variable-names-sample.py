# A variable can have a short name (like x and y) or a more descriptive name (age, carname, total_volume).

# Legal variable names:
myvar = "John"
my_var = "John"
_my_var = "John"
myVar = "John"
MYVAR = "John"
myvar2 = "John"

print(myvar)
print(my_var)
print(_my_var)
print(myVar)
print(MYVAR)
print(myvar2)

# Illegal variable names:
# 2myvar = "John"    # Variable name cannot start with a number
# my-var = "John"    # Variable name cannot contain hyphens
# my var = "John"    # Variable name cannot contain spaces

# Note: Variable names are case-sensitive (myvar, Myvar, MYVAR are different variables)
