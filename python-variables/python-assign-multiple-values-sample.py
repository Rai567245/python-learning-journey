# Many values to multiple variables
num1, num2, num3 = 5, 10, 15

print("num1:", num1)

# One value to multiple variables

num1 = num2 = num3 = 15
print("num1:", num1)
print("num2:", num2)
print("num3:", num3)

# Unpacking a collection

numCollection = [5, 10, 15]
num1, num2, num3 = numCollection

print("num1:", num1)
print("num2:", num2)
print("num3:", num3)